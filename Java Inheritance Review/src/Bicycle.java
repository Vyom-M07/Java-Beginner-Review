
public class Bicycle extends Vehicle{
	
	private int wheels = 2;
	private int pedals = 2;
	
	public int getWheels() {
		return wheels;
	}
	public void setWheels(int wheels) {
		this.wheels = wheels;
	}
	public int getPedals() {
		return pedals;
	}
	public void setPedals(int pedals) {
		this.pedals = pedals;
	}
}
